math = {
 "Матвей",
 "Евгения",
 "Михаил",
 "Максим",
 "Наталья"
}

phys = {
 "Максим",
 "Матвей",
 "Александр"
}

win = (phys | math)
win = list(win)
math = (math & phys)
del(phys)
print(f"Все призёры: {win}")
print(f"Призёры обеих дисциплин: {math}")
print(f"Обновленный список призёров по математике: {math}")
